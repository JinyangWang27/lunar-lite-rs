// tools/lunar-lite-reference/scripts/dump-year-info.mjs

import { mkdir, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { LunarYear, Solar } from "lunar-typescript";

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const repoRoot = resolve(__dirname, "../../..");

const MIN_YEAR = 1850;
const MAX_YEAR = 2150;

function padArray(values, length, padValue = 0) {
    if (values.length > length) {
        throw new Error(`array too long: ${values.length} > ${length}`);
    }
    return [...values, ...Array(length - values.length).fill(padValue)];
}

function rustArray(values) {
    return `[${values.join(", ")}]`;
}

function dumpYear(year) {
    const lunarYear = LunarYear.fromYear(year);
    const months = lunarYear.getMonthsInYear();

    if (months.length !== 12 && months.length !== 13) {
        throw new Error(`unexpected month count for ${year}: ${months.length}`);
    }

    const firstMonth = months.find((month) => month.getMonth() === 1);

    if (!firstMonth) {
        throw new Error(`cannot find lunar month 1 for year ${year}`);
    }

    const newYearSolar = Solar.fromJulianDay(firstMonth.getFirstJulianDay());

    const monthCodes = months.map((month) => month.getMonth());
    const monthDays = months.map((month) => month.getDayCount());

    const leapMonth = lunarYear.getLeapMonth();

    return {
        year,
        new_year: {
            year: newYearSolar.getYear(),
            month: newYearSolar.getMonth(),
            day: newYearSolar.getDay()
        },
        leap_month: leapMonth > 0 ? leapMonth : null,
        month_count: months.length,
        month_codes: monthCodes,
        month_days: monthDays
    };
}

function renderRust(years) {
    const items = years.map((info) => {
        const monthCodes = padArray(info.month_codes, 13, 0);
        const monthDays = padArray(info.month_days, 13, 0);

        const leapMonth = info.leap_month === null
            ? "None"
            : `Some(${info.leap_month})`;

        return `    LunarYearInfo {
        year: ${info.year},
        new_year: SolarDate {
            year: ${info.new_year.year},
            month: ${info.new_year.month},
            day: ${info.new_year.day},
        },
        leap_month: ${leapMonth},
        month_count: ${info.month_count},
        month_codes: ${rustArray(monthCodes)},
        month_days: ${rustArray(monthDays)},
    },`;
    }).join("\n");

    return `// @generated by tools/lunar-lite-reference/scripts/dump-year-info.mjs
// Do not edit manually.

use crate::date::SolarDate;
use crate::year_info::LunarYearInfo;

pub(crate) const MIN_LUNAR_YEAR: i32 = ${MIN_YEAR};
pub(crate) const MAX_LUNAR_YEAR: i32 = ${MAX_YEAR};

pub(crate) const YEAR_INFOS: [LunarYearInfo; ${years.length}] = [
${items}
];
`;
}

const years = [];

for (let year = MIN_YEAR; year <= MAX_YEAR; year++) {
    years.push(dumpYear(year));
}

const rustPath = resolve(repoRoot, "src/generated/year_info.rs");
const jsonPath = resolve(repoRoot, "tests/fixtures/year_info.json");

await mkdir(dirname(rustPath), { recursive: true });
await mkdir(dirname(jsonPath), { recursive: true });

await writeFile(rustPath, renderRust(years), "utf8");
await writeFile(jsonPath, JSON.stringify(years, null, 2) + "\n", "utf8");

console.log(`Generated ${rustPath}`);
console.log(`Generated ${jsonPath}`);